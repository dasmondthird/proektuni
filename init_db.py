import sqlite3
import os
import sys

from config import DB_PATH


def init_database() -> None:
    """Создаёт БД, таблицы и заполняет начальными данными. Идемпотентна."""

    db_dir = os.path.dirname(DB_PATH)
    if db_dir and not os.path.exists(db_dir):
        os.makedirs(db_dir)

    if os.path.exists(DB_PATH):
        os.remove(DB_PATH)
        print("  Старая БД удалена.")

    print(f"Создание базы данных: {DB_PATH}")

    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()

    try:
        _create_tables(cursor)
        print("  Таблицы созданы.")

        _populate_hs_codes(cursor)
        _populate_preferences(cursor)
        _populate_customs_fees(cursor)

        conn.commit()
        print("\nБаза данных успешно создана и заполнена!")
    except Exception as e:
        conn.rollback()
        print(f"\nОшибка при создании БД: {e}", file=sys.stderr)
        sys.exit(1)
    finally:
        conn.close()


# ============================================================
# Создание таблиц
# ============================================================

def _create_tables(cursor: sqlite3.Cursor) -> None:
    """Создаёт три таблицы: hs_codes, preferences, customs_fees."""

    cursor.execute("DROP TABLE IF EXISTS hs_codes")
    cursor.execute("DROP TABLE IF EXISTS preferences")
    cursor.execute("DROP TABLE IF EXISTS customs_fees")

    cursor.execute("""
        CREATE TABLE hs_codes (
            code TEXT PRIMARY KEY,
            group_number INTEGER NOT NULL,
            description TEXT NOT NULL,
            duty_type TEXT NOT NULL,
            duty_rate REAL NOT NULL,
            vat_rate REAL NOT NULL DEFAULT 22.0,
            excise REAL DEFAULT 0,
            keywords TEXT DEFAULT '',
            function_features TEXT DEFAULT '',
            material_features TEXT DEFAULT '',
            application_area TEXT DEFAULT '',
            updated_at DATE
        )
    """)

    cursor.execute("""
        CREATE TABLE preferences (
            country_code TEXT PRIMARY KEY,
            country_name TEXT NOT NULL,
            preference_type TEXT NOT NULL,
            coefficient REAL NOT NULL
        )
    """)

    cursor.execute("""
        CREATE TABLE customs_fees (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            min_value REAL NOT NULL,
            max_value REAL NOT NULL,
            fee REAL NOT NULL
        )
    """)


